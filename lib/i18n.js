function deepMerge(base, override) {
  const result = { ...base };
  Object.entries(override || {}).forEach(([key, value]) => {
    result[key] = value && typeof value === "object" && !Array.isArray(value)
      ? deepMerge(base[key] || {}, value)
      : value;
  });
  return result;
}

export function getMessages(locale = DEFAULT_LOCALE) {
  const normalizedLocale = SUPPORTED_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;
  const language = normalizedLocale.split("-")[0];
  return [
    fullLocaleOverrides[language],
    fullLocaleOverrides[normalizedLocale],
    localizedOverrides[language],
    localizedOverrides[normalizedLocale],
    settingsPanelOverrides[language],
    settingsPanelOverrides[normalizedLocale],
  ].reduce((catalog, override) => deepMerge(catalog, override || {}), messages[DEFAULT_LOCALE]);
}

export function translate(locale, key, values = {}) {
  const catalog = getMessages(locale);
  const value = key.split(".").reduce((node, part) => node?.[part], catalog);
  const template = typeof value === "string" ? value : key;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(values[name] ?? ""));
}

function flattenMessages(node, prefix = "") {
  return Object.entries(node || {}).flatMap(([key, value]) => {
    const nextKey = prefix ? `${prefix}.${key}` : key;
    if (value && typeof value === "object" && !Array.isArray(value)) {
      return flattenMessages(value, nextKey);
    }
    return [[nextKey, value]];
  });
}

export function auditLocaleCoverage(locale) {
  const baseEntries = flattenMessages(messages[DEFAULT_LOCALE]);
  const catalog = getMessages(locale);
  const ignoredPrefixes = ["common.suhas"];
  const ignoredValues = new Set(["SB Chat", "OpenAI", "JSON", "API", "URL", "WebRTC", "Top K", "Error"]);

  const inheritedKeys = baseEntries
    .filter(([key, english]) => typeof english === "string")
    .filter(([key]) => !ignoredPrefixes.some((prefix) => key.startsWith(prefix)))
    .filter(([, english]) => !ignoredValues.has(english))
    .filter(([key, english]) => {
      const localized = key.split(".").reduce((node, part) => node?.[part], catalog);
      return localized === english;
    })
    .map(([key]) => key);

  return {
    locale,
    totalKeys: baseEntries.length,
    inheritedKeys,
    inheritedCount: inheritedKeys.length,
    complete: inheritedKeys.length === 0,
  };
}

export function getTextDirection(locale = DEFAULT_LOCALE) {
  return ["ar", "he", "fa", "ks", "sd", "ur"].includes(locale.split("-")[0]) ? "rtl" : "ltr";
}
