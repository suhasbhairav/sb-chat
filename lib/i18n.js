export const SUPPORTED_LOCALES = Array.from(new Set([...EUROPEAN_LOCALES, ...ASIAN_LOCALES, ...INDIAN_LOCALES]));

function makeLabel(locale) {
  try {
    const displayNames = new Intl.DisplayNames([locale, DEFAULT_LOCALE], { type: "language" });
    const regionNames = new Intl.DisplayNames([locale, DEFAULT_LOCALE], { type: "region" });
    const [language, region] = locale.split("-");
    const languageName = displayNames.of(language) || locale;
    const regionName = region ? ` (${regionNames.of(region) || region})` : "";
    return `${languageName}${regionName}`;
  } catch {
    return locale;
  }
}

